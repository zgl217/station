package cn.springmvc.model;


/**
 * lte表
 * @author zgl
 * 2014年2月11日
 */
public class Lte {

	private String id;
	private String cellName;
	private String enodeName;
	private String gcLng;
	private String gcLat;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCellName() {
		return cellName;
	}
	public void setCellName(String cellName) {
		this.cellName = cellName;
	}
	public String getEnodeName() {
		return enodeName;
	}
	public void setEnodeName(String enodeName) {
		this.enodeName = enodeName;
	}
	public String getGcLng() {
		return gcLng;
	}
	public void setGcLng(String gcLng) {
		this.gcLng = gcLng;
	}
	public String getGcLat() {
		return gcLat;
	}
	public void setGcLat(String gcLat) {
		this.gcLat = gcLat;
	}
	
}
